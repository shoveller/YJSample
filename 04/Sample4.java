class Sample4
{
    public static void main(String[] args)
    {
        int num1 = 10;
        int num2 = 5;
        System.out.println("num1 과 num2 로 다양한 연산을 수행합니다.");
        System.out.println("num1+num2 는 " + (num1+num2) + "입니다.");
        System.out.println("num1-num2 는 " + (num1-num2) + " 입니다.");
        System.out.println("num1*num2 는 " + (num1*num2) + " 입니다.");
        System.out.println("num1/num2 는 " + (num1/num2) + " 입니다.");
        System.out.println("num1%num2 는 " + (num1%num2) + " 입니다.");
    }
}